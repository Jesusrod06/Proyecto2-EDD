    private void actualizarComboBox(Lista titulos) {
        modeloCombo.removeAllElements();

        for (int i = 0; i < titulos.getSize(); i++) {
            String titulo = (String) titulos.getValor(i);
            modeloCombo.addElement(titulo);
        }

    }

