    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String autor = autorB.getText();
        detalleR.setText("");
        if (!autor.isBlank() && !autor.isEmpty()) {
            Lista titulos = sistemaInv.buscarInvestigacionesPorAutorCoincidencia(autor);
            if (titulos.getSize() > 0) {
                this.actualizarComboBox(titulos);
                
            } else {
                JOptionPane.showMessageDialog(null, "No se encontraron titulos asociados con esa palabra clave.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se escribio nada en el Input, debes escribir una palabra.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (modeloCombo.getSize() > 0) {
            String tituloSeleccionado = (String) titulosR.getSelectedItem();
            Resumen resumen = sistemaInv.buscarResumenPorTitulo(tituloSeleccionado);
            detalleR.setText(resumen.toString());
        }else{
            JOptionPane.showMessageDialog(null, "No hay ningun titulo seleccionado.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

